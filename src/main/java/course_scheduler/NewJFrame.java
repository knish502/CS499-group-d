    private int findCourseIndex(String input)
    {
        int index = -1;
        if (allCourses.size() > 0)
        {
            for (int i = 0; i < allCourses.size(); i++)
            {
                if ((input) == (allCourses.get(i).returnCourseID()))
                {
                    index = i;
                    i = allCourses.size() + 1;
                }
            }
        }
        return index;
    }
    
    
