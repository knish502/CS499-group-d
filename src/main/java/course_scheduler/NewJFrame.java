    private void RemoveCourseButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveCourseButton1ActionPerformed
        // TODO add your handling code here:
        String removeCourseID = "";
        int index = -1;
        if ((RemoveCourse_ChooseID1.getSelectedItem().toString()) != "Select Course")
        {
            removeCourseID = RemoveCourse_ChooseID1.getSelectedItem().toString();
            index = findCourseIndex(removeCourseID);
        }
        
        if (index != -1)
        {
            allCourses.remove(index);
            EditCourse_ChooseID.removeItem(removeCourseID);
            RemoveCourse_ChooseID1.removeItem(removeCourseID);
            DefaultTableModel model = (DefaultTableModel) FilePreview2.getModel();
            int numRows = model.getRowCount();
            boolean allValuesFilled = false;
            for (int i = 0; i < numRows; i++)
            {
                if (model.getValueAt(i, 0) == removeCourseID)
                {
                    model.removeRow(i);
                    i = numRows + 1;
                }
            }
        
        }
        
        
        
    }//GEN-LAST:event_RemoveCourseButton1ActionPerformed


